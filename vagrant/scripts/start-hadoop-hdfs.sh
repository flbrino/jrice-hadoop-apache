#!/bin/sh
#################################################################################################
# Start HDFS
# 
# This script Starts HDFS Services - namenode, secondary namenode, and datanode
#
# Created By Joe Rice - 2/6/2018
#################################################################################################

echo "."
echo "*********************************************************************************"
echo "   Start HDFS Name Node..."
echo "*********************************************************************************"

cd /opt/hadoop-2.8.1/sbin

./hadoop-daemon.sh start namenode

echo "."
echo "*********************************************************************************"
echo "   Start HDFS Secondary Name Node..."
echo "*********************************************************************************"

./hadoop-daemon.sh start secondarynamenode

echo "."
echo "*********************************************************************************"
echo "   Start HDFS Data Node..."
echo "*********************************************************************************"

./hadoop-daemon.sh start datanode